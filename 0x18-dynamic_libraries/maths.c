#include <stdio.h>
/**
 * add - function that sum two numbers
 * @a: number 1
 * @b: number 2
 * Return: the sum of both numbers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function that rest two numbers
 * @a: number 1
 * @b: number 2
 * Return: the sub of both numbers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function that multiplicate two numbers
 * @a: number 1
 * @b: number 2
 * Return: the multiplicate both numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function that divide two numbers
 * @a: number 1
 * @b: number 2
 * Return: the div of both numbers
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function that mod two numbers
 * @a: number 1
 * @b: number 2
 * Return: the mod of both numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
